import express from "express";
import authUser from "../middlewares/authUser.js";
import { getAllOrders, getUserOrders, placeOrderCOD, updateOrderStatus, trackOrder, getOrderById, cancelOrder } from "../controllers/orderController.js";
import authSeller from "../middlewares/authSeller.js";
const orderRouter=express.Router();
orderRouter.post("/cod",authUser,placeOrderCOD);
orderRouter.get("/user",authUser,getUserOrders);
orderRouter.get("/seller",authSeller,getAllOrders);
orderRouter.post("/status",authSeller,updateOrderStatus);
orderRouter.post("/cancel",authUser,cancelOrder);
orderRouter.get("/track/:trackingNumber",trackOrder);
orderRouter.get("/:orderId",authUser,getOrderById);


export default orderRouter;